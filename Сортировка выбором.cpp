#include <bits/stdc++.h>
using namespace std;
void selection_sort(vector<int>& a){
    int n = a.size();               // получаем число элементов массива
    for(int i = 0; i < n; i++){     // внешний цикл по позициям, куда будем ставить минимум
        int mn = i;                 // предполагаем, что текущая позиция содержит минимум
        for(int j = i+1; j < n; j++) // ищем меньший элемент в оставшейся части
            if(a[j] < a[mn])        // если найден меньший — обновляем индекс минимума
                mn = j;
        swap(a[i], a[mn]);          // помещаем найденный минимум на позицию i
    }
}
int main(){ 
    vector<int> a = {64,25,12,22,11}; // входной массив для примера
    selection_sort(a);                // сортируем массив
    for(int x : a) cout << x << ' ';  // выводим результат в консоль
    cout << '\\n';
}
