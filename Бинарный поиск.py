def binary_search(a, x):
    l, r = 0, len(a) - 1                  # устанавливаем границы поиска (лево и право)
    while l <= r:                         # пока интервал не пуст
        m = (l + r) // 2                 # берём середину (без переполнения в Python)
        if a[m] == x: return m           # найден — возвращаем индекс
        if a[m] < x: l = m + 1           # искомое справа от середины — сдвигаем левую границу
        else: r = m - 1                  # искомое слева — сдвигаем правую границу
    return -1                             # не найден

if __name__ == '__main__':
    a = [1,3,5,7,9]                      # массив должен быть отсортирован
    print(binary_search(a, 5))           # ожидаемый индекс 2
