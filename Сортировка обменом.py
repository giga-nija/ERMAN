def bubble(a):
    n = len(a)                         # размер массива
    for i in range(n):                 # повторяем проходы по массиву
        swapped = False               # флаг для раннего завершения, если массив уже отсортирован
        for j in range(0, n - i - 1): # внутренний цикл сравнивает пары соседних элементов
            if a[j] > a[j + 1]:       # если порядок элементов неверный
                a[j], a[j + 1] = a[j + 1], a[j]  # меняем их местами — это "пузырёк"
                swapped = True        # отмечаем, что был обмен
        if not swapped:               # если за проход не было обменов — массив отсортирован
            break

if __name__ == '__main__':
    a = [5, 1, 4, 2, 8]               # пример входа
    bubble(a)                         # сортируем
    print(a)                          # вывод результата
