def insertion(a):
    for i in range(1, len(a)):           # начинаем со второго элемента, строим отсортированную часть слева
        key = a[i]                       # сохраняем значение текущего вставляемого элемента
        j = i - 1                        # начинаем сравнение с предыдущего элемента
        while j >= 0 and a[j] > key:     # сдвигаем элементы вправо, пока они больше ключа
            a[j + 1] = a[j]              # сдвиг большего элемента на одну позицию вправо
            j -= 1                       # переходим к предыдущему элементу
        a[j + 1] = key                   # вставляем ключ в найденную позицию

if __name__ == '__main__':
    a = [12, 11, 13, 5, 6]              # пример входа
    insertion(a)                        # сортируем
    print(a)                            # результат
